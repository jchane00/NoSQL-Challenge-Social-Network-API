const mongoose = require("mongoose");

const { model, Schema, Types } = mongoose;

const reactionSchema = new Schema(
  {
    reactionId: {
      type: Types.ObjectId,
      default: () => new Types.ObjectId(),
    },
    reactionBody: {
      type: String,
      required: true,
      maxlength: 280,
    },
    username: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
      get: (currentValue) => currentValue.toISOString(),
    },
  },
  {
    toJSON: {
      getters: true,
    },
  }
);

const schema = new Schema(
  {
    thoughtText: {
      type: String,
      required: true,
      minlength: 1,
      maxlength: 280,
    },
    createdAt: {
      type: Date,
      default: Date.now,
      get: (currentValue) => currentValue.toISOString(),
    },
    username: {
      type: String,
      required: true,
    },
    reactions: [reactionSchema],
  },
  {
    toJSON: {
      virtuals: true,
      getters: true,
    },
  }
);

schema.virtual("reactionCount").get(function () {
  return this.reactions.length;
});

module.exports = model("thoughts", schema);
